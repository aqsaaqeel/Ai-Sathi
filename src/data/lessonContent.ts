// Helper function to get chapter by ID
export function getChapterById(subject: "maths" | "science", chapterId: string): Chapter | undefined {
  const chapters = subject === "maths" ? mathsChapters : scienceChapters;
  return chapters.find(ch => ch.id === chapterId);
}

// Helper function to get lesson by ID
export function getLessonById(
  subject: "maths" | "science",
  chapterId: string,
  lessonId: string
): Lesson | undefined {
  const chapter = getChapterById(subject, chapterId);
  return chapter?.lessons.find(l => l.id === lessonId);
}
