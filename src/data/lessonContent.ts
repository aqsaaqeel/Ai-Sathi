/**
 * LESSON CONTENT DATA
 * 
 * Structured learning content for Duolingo-style card-based lessons
 * Each lesson has multiple cards with explanations, examples, and exercises
 */

export interface LessonCard {
  id: string;
  type: "explanation" | "example" | "practice" | "tip";
  title: string;
  titleHindi?: string;
  titleKannada?: string;
  content: string;
  contentHindi?: string;
  contentKannada?: string;
  visual?: string; // Emoji or simple illustration
  question?: string;
  questionHindi?: string;
  questionKannada?: string;
  options?: string[];
  optionsHindi?: string[];
  optionsKannada?: string[];
  correctAnswer?: string;
  correctAnswerHindi?: string;
  correctAnswerKannada?: string;
  explanation?: string;
  explanationHindi?: string;
  explanationKannada?: string;
}

export interface Lesson {
  id: string;
  title: string;
  titleHindi: string;
  titleKannada?: string;
  description: string;
  descriptionHindi?: string;
  descriptionKannada?: string;
  difficulty: "easy" | "medium" | "hard";
  duration: string; // "5 min", "10 min"
  cards: LessonCard[];
}

export interface Chapter {
  id: string;
  title: string;
  titleHindi: string;
  titleKannada?: string;
  lessons: Lesson[];
}

// Helper function to get text in selected language
export function getLocalizedText(
  english: string,
  hindi?: string,
  kannada?: string,
  language: "en" | "hi" | "kn" = "en"
): string {
  if (language === "hi" && hindi) return hindi;
  if (language === "kn" && kannada) return kannada;
  return english;
}

